<p>TinyWeb to prosty framework PHP do tworzenia stron www. Jego tworzeniu przyświeca reguła
    <?php echo_link_remote('http://pl.wikipedia.org/wiki/KISS_(regu%C5%82a)', 'KISS', 'Keep It Simple, Stupid') ?>.
    Chodzi o prostotę, bez tworzenia struktur, które mogą być przydatne w przypadku dużych
    serwisów, ale dla prostych stron są objawem przerostu formy nad treścią.
</p>
<p>Powstał na własne potrzeby, z czasem poprawiany, otrzymuje nowe funkcjonalności 
    zależnie od zapotrzebowania, przy czym cały czas staram się utrzymywać prostotę.
    Prostota obsługi to główny cel.</p>
<p>Cały czas staram się poprawiać i udoskonalać generowany kod HTML, tak aby spełniał najnowsze standardy. 
    Ze zmianami oraz najbliższymi planami można zapoznać się na stronie <?php echo_link('download', $dict['download']) ?>.</p>
<p>Trzeba pamiętać, że framework ten służy mi nie tylko do tworzenia stron, pozwala także na 
    testowanie i poznawanie możliwości PHP, HMTL, JS oraz CSS u samych podstaw.</p>
<p>Chcąc skorzystać z tego frameworka, wymagana jest minimalna wiedza na temat PHP oraz HTML.</p>

