<h3 style="margin-top:30px;">Proponowane rangi w gildii</h3>
<ol>
    <li>
        <h4>Guild Master</h4>
        <p>Osoba odpowiedzialna za gildię.</p>
        <p>Kolejka do lootu na rajdach 25-man: 1.</p>
    </li> 
    <li>
        <h4>Oficer</h4>
        <p>Osoby odpowiedzialne za porządek w gildii, służące radą i pomocą Guild Masterowi. 
            Prowadzą i organizują poszczególne grupy rajdowe 10 osobowe, 
            bądź są odpowiedzialne za inne sprawy dotyczące gildii.</p>
        <p>Kolejka do lootu na rajdach 25-man: 1.</p>        
    </li> 
    <li>
        <h4>Weteran</h4>
        <p>Doświadczony, stały członek gildii. Każdy "Weteran" ma dostęp do flaszek z gildii.</p>
        <p>Kolejka do lootu na rajdach 25-man: 1.</p>  
    </li> 
    <li>
        <h4>Member</h4>
        <p>Stały członek gildii. Każdy "Member" należy do jednej z grup rajdowych.</p>
        <p>Kolejka do lootu na rajdach 25-man: 1.</p>  
    </li> 
    <li>
        <h4>Nowy</h4>
        <p>Nowy członek gildii. Przyjęty czasowo, aż się wykaże poprzez udział w rajdach.</p>
        <p>Kolejka do lootu na rajdach 25-man: 2.</p>
    </li> 
    <li>
        <h4>Alt</h4>
        <p>Każdy GM, Oficer oraz Member mogą posiadać 1 Alta. Jeśli potrzebna jest
            osoba na zastępstwo w jednej z grup rajdowych, Alt ma pierwszeństwo przed gościem.</p>
        <p>Kolejka do lootu na rajdach 25-man: 2.</p>
    </li>
    <li>
        <h4>Gość</h4>
        <p>Każdy GM, Oficer, Member oraz Nowy mogą posiadać dowolną ilość gości w gildii.
            Są to postacie, które zapraszane są bardziej "do towarzystwa", niż do rajdowania.
            Zapraszane na rajdy w przypadku braku innych chętnych.</p>
        <p>Kolejka do lootu na rajdach 25-man: 3.</p>
    </li>
    <li>
        <h4>Karniak</h4>
        <p>Ranga uniemożliwiająca wypowiadanie się na czacie gildii, otrzymywana za łamanie
            regulaminu gildii lub serwera.</p>
        <p>Kolejka do lootu na rajdach 25-man: 4.</p>     
    </li>
</ol>
<p>Zmiana rang jest ustalana pomiędzy oficerami oraz liderem gildii, wyjątkiem jest zmiana na Karniaka.</p>
<h3>Zasady lootu</h3>
<ol>
    <li>
        <p>Loot licytowany jest wedle kolejki do lootu.</p>
    </li>
    <li>
        <p>Obowiązuje zasada najpierw Main Spec (kolejka 1-4), potem Off (kolejka 1-4).</p>
    </li>
    <li>
        <p>Jeśli gracz dobrowolnie wybrał się na rajd Altem bądź Gościem, 
            obowiązuje go kolejka do lootu.</p>
    </li>
    <li>
        <p>Jeśli gracz z potrzeby rajdu idzie Altem bądź Gościem, ma prawo do lootu
            na równi z Memberami.</p>
    </li>
</ol>