<h4>Feral</h4>
<p>Kiedy odpala berserka, po pierwszych 3-4 sekundach trzeba użyć Dispersion, 
    żeby uniknąć nuke - przez te początkowe sekundy on zazwyczaj jeszcze nie 
    nukuje, tylko zbiera cb i debuffy na celu.</p>
<h4>Szaman</h4>
<p>Renew jest BARDZO słabe, bo szaman od razu zdejmie Purgem. Niezależnie od comba.</p>
<h4>Druid + rogal</h4>
<p>Trzeba wiedzieć, kiedy spamować holy nova, żeby go odkryć, ustawiać się w 
    widoku poza zasięgiem. Trinket blinda/cyklonu.</p>
<h4>DK</h4>
<p>Silence FTW, jak najszybciej, żeby nie zaczął od anti-magic shell, itp. 
    PW:S, renew, abolish disease dobre.</p>
<h4>Teamy spamujące dispell na VT</h4>
<p>Fear w dispellera lub czekać na więcej debuffów od partnera, lub dispell,
    potem VT.</p>

